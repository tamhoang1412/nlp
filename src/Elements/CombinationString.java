package Elements;

public class CombinationString {
    public String string;
    public int from;
    public int to;

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }
}
